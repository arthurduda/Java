package repetiçãofor;
public class RepetiçãoFor {
    public static void main(String[] args) {
            for (int cc=0; cc<=100; cc+=10) {
                System.out.print(cc+" ");
            }
            System.out.print("\n");
            for (int c=100; c>=0; c-=10){
                System.out.print(c+" ");
            }
            System.out.print("\n");
        }       
    }    
